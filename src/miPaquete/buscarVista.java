    public static int mostrarMensajeEmergente(String titulo, String mensaje){
        Object[] options = { "Aceptar", "Cancelar" };
        int resultadoDeLaPregunta = -1;
        
        if(titulo.equals("ALERTA")){
            resultadoDeLaPregunta = JOptionPane.showOptionDialog(null, "¬øEstas seguro de eliminar al Alumno?", "Alerta",JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE,null, options, options[0]);
           
            if(resultadoDeLaPregunta == JOptionPane.YES_OPTION){
                  
                
                return resultadoDeLaPregunta;
                
            }else{
                
                mostrarMensajeEmergente("Mensaje", "No se elimin√≥ el usuario");
            }
        
        
        }else
            JOptionPane.showMessageDialog(null, mensaje, titulo, JOptionPane.OK_OPTION);
        return  resultadoDeLaPregunta;
    }
    
    
    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
      
        if(editButton.getText().equals("Editar")){
            jTextFieldCodigo.setEnabled(true);
            jTextFieldNombre.setEnabled(true);
            jTextFieldCarrera.setEnabled(true);
            jTextFieldPromedio.setEnabled(true);
            jTextFieldSemestre.setEnabled(true);
            repaint();
            editButton.setText("Aceptar Edici√≥n");
        }else{
            
            jTextFieldCodigo.setEnabled(false);
            jTextFieldNombre.setEnabled(false);
            jTextFieldCarrera.setEnabled(false);
            jTextFieldPromedio.setEnabled(false);
            jTextFieldSemestre.setEnabled(false);
            repaint();
            
            int codigo=0;
            int semestre=0;
            double promedio=0;
            String nombre = jTextFieldNombre.getText();
            String carrera = jTextFieldCarrera.getText();
            try {
                codigo = Integer.parseInt(jTextFieldCodigo.getText());
                semestre = Integer.parseInt(jTextFieldSemestre.getText());
                promedio = Double.parseDouble(jTextFieldPromedio.getText());

            } catch (Exception e) {
                System.out.println(e);
                System.out.println("Problema al convertir de String a int o double");
            }
            
            int codigoAlumnoEditar = Integer.parseInt(codigoBuscadoTextField.getText());
            
            Controlador.guardarAlumnoEditado(new Alumno(codigo, nombre, carrera, semestre, promedio),codigoAlumnoEditar);
            editButton.setText("Editar");
            mostrarMensajeEmergente("Editar", "Alumno Editado Exitosamente!!!");
            
        }
       
       
    }//GEN-LAST:event_editButtonActionPerformed

    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        
        
        if(!codigoBuscadoTextField.getText().equals("")){
            int codigoBuscado;
            try {
                
                codigoBuscado =  Integer.parseInt(codigoBuscadoTextField.getText());
                
                Alumno alumno = Controlador.buscarAlumno(codigoBuscado);
                
                if(alumno.getCodigo()!=-1){
                    //mostrar en la vista
                    jTextFieldCodigo.setText(Integer.toString(alumno.getCodigo()));
                    jTextFieldNombre.setText(alumno.getNombre());
                    jTextFieldCarrera.setText(alumno.getCarrera());
                    jTextFieldPromedio.setText(""+alumno.getPromedio());
                    jTextFieldSemestre.setText(""+alumno.getSemestre());
                    
                    
                }else{
                    mostrarMensajeEmergente("Buscar", "No se encontr√≥ el alumno");
                    jTextFieldCodigo.setText("");
                    jTextFieldCarrera.setText("");
                    jTextFieldNombre.setText("");
                    jTextFieldPromedio.setText("");
                    jTextFieldSemestre.setText("");
                    jTextFieldCodigo.setEnabled(false);
                    jTextFieldNombre.setEnabled(false);
                    jTextFieldCarrera.setEnabled(false);
                    jTextFieldPromedio.setEnabled(false);
                    jTextFieldSemestre.setEnabled(false);
                    editButton.setText("Editar");
                    repaint();
                }
                
                
            } catch (Exception e) {
                mostrarMensajeEmergente("Error", "El c√≥digo debe de ser un n√∫mero ");
            }
             
        }else{
            
            mostrarMensajeEmergente("Error", "Introduce un c√≥digo para buscar");
        
        }
        
        
        
        
    }//GEN-LAST:event_buscarButtonActionPerformed

